class DeepSort(object):
    def __init__(
            self, model_path, max_dist=.2, min_confidence=.3, nms_max_overlap=.5, max_iou_distance=0.7,
            max_age=70, n_init=3, nn_budget=100, use_cuda=True
    ):
        self.min_confidence = min_confidence
        self.nms_max_overlap = nms_max_overlap

        self.extractor = Extractor(model_path, use_cuda=use_cuda)

        max_cosine_distance = max_dist
        nn_budget = 100
        metric = NearestNeighborDistanceMetric("cosine", max_cosine_distance, nn_budget)
        self.tracker = Tracker(metric, max_iou_distance=max_iou_distance, max_age=max_age, n_init=n_init)

    def update(self, bbox_xywh, confidences, ori_img, cls_ids):
        self.height, self.width = ori_img.shape[:2]
        # generate detections
        features = self._get_features(bbox_xywh, ori_img)
        if len(features) == 0:
            return np.array([])
        bbox_tlwh = self._xywh_to_tlwh(bbox_xywh)

        # print("len(features): ", len(features))
        # print("len(confidences): ", len(confidences))
        # print("len(bbox_tlwh): ", len(bbox_tlwh))
        detections = [Detection(bbox_tlwh[i], conf, features[i], cls_ids[i]) for i, conf in enumerate(confidences) if
                      conf > self.min_confidence]

        # run on non-maximum supression
        boxes = np.array([d.tlwh for d in detections])
        scores = np.array([d.confidence for d in detections])
        indices = non_max_suppression(boxes, self.nms_max_overlap, scores)
        detections = [detections[i] for i in indices]
        # for dt in detections:
        #     print("detections", dt.clsid)

        # update tracker
        self.tracker.predict()
        self.tracker.update(detections)

        # output bbox identities
        outputs = []
        for track in self.tracker.tracks:
            if not track.is_confirmed() or track.time_since_update > 1:
                continue
            box = track.to_tlwh()
            x1, y1, x2, y2 = self._tlwh_to_xyxy(box)
            track_id = track.track_id
            # cls_id = track.cls_id
            # outputs.append(np.array([x1,y1,x2,y2,track_id, cls_id], dtype=np.int))
            outputs.append(np.array([x1, y1, x2, y2, track_id], dtype=np.int))
        if len(outputs) > 0:
            outputs = np.stack(outputs, axis=0)
        return outputs

